public class Conver {

    public double convertendoParaDolares(double reais) {
        return reais * 0.17925;
    }

    public double convertendoParaEuro(double reais) {
        return reais * 0.15418;
    }

    public double convertendoMetrosParaKm(double metros) {
        return metros * 0.001;
    }

    public double convertendoMetrosParaCm(double metros) {
        return metros * 100;
    }

    public double convertendoMetrosParaMilimetros(double metros) {
        return metros * 1000;
    }
}
